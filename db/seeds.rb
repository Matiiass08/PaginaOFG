Juego.create(
  nombre: "Cachipun Evolución o Juego de la ameba", 
  descripcion: "Es un juego para romper el hielo, donde en un terreno definido los participantes juegan cachipun (piedra, papel o tijera) entre ellos. A medida que transcurre el juego se debe imitar uno de los siguientes animales: ameba, cangrejo, conejo, gorila y humano. Todos comienzan siendo amebas. Al ganar una partida de cachipun evolucionas a cangrejo, luego a conejo y luego en gorila. Si ganas como gorila, evolucionas a humano y ganas el juego. Cabe destacar que solo pueden enfrentarse con personas que estén en el mismo nivel de evolución, es decir, amebas con amebas, cangrejos con cangrejos, conejos con conejos y gorilas con gorilas. Si en cualquier etapa de este juego pierdes, vuelves a ser ameba. Por lo tanto, para ganar se debe ganar 5 veces seguidas el cachipun. El juego termina cuando se llega a cierta cantidad de humanos o se acaba el tiempo.
Objetivos: Romper el hielo entre gente que no se conocen, perder la timidez al simular ser cualquiera de estos animales, mejorar la interacción social y una competencia amistosa."
)

