  descripcion: "Los participantes forman un círculo y colocan su mano derecha sobre la mano izquierda del compañero, creando una cadena de manos. Mientras cantan una canción, después de cada palabra, una persona da un aplauso con su mano derecha sobre la mano derecha de la persona a su izquierda, manteniendo la cadena en movimiento. La canción es: 'Toma, tomate, toma, lo, IA, IA, IO PLOP.' Cuando llegan al 'PLOP,' la persona que debería recibir el aplauso debe mover su mano para evitar ser tocada. Si es tocada, esa persona pierde y sale del círculo. El juego continúa hasta que solo quede una persona en el círculo."
